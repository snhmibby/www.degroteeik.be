export function IconButton(parent: HTMLElement, html: string, fn): HTMLElement {
	let btn = document.createElement('span')
	btn.innerHTML = html
	btn.className = 'icon-button'
	btn.onclick = fn
	parent.appendChild(btn)
	return btn
}

